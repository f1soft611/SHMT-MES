-- ========================================
-- ERP 프로시저 호출 예제
-- ========================================

-- 1. 기본 재고 조회 (날짜별)
EXEC CYMC_IF_SLGWHStocklistQuery
    @DateFr = '20260101',
    @DateTo = '20260213',
    @ItemNo = NULL;

-- 2. 특정 품번으로 재고 조회
EXEC CYMC_IF_SLGWHStocklistQuery
    @DateFr = '20260101',
    @DateTo = '20260213',
    @ItemNo = 'ITEM001';

-- ========================================
-- 프로시저 파라미터 정보
-- ========================================
-- @DateFr    : 조회시작일자 (YYYYMMDD) - 필수
-- @DateTo    : 조회종료일자 (YYYYMMDD) - 필수
-- @ItemNo    : 품번 (선택) - NULL 입력 시 전체 조회

-- ========================================
-- 프로시저 반환 결과 컬럼 정보
-- ========================================
-- itemSeq    : 품목코드 (int)
-- itemNo     : 품번 (nvarchar 200)
-- WHSeq      : 창고코드 (int)
-- WHName     : 창고명 (nvarchar 200)
-- STOCKQTY   : 재고수량 (decimal 19,5)
-- DateFr     : 조회조건From (nchar 8)
-- DateTo     : 조회조건To (nchar 8)

-- ========================================
-- 테이블 매핑 정보
-- ========================================
-- ERP 테이블: CYMC_IF_SLGWHStocklistQuery (프로시저)
-- MES 테이블: 없음 (조회 전용, 데이터 저장 안 함)

